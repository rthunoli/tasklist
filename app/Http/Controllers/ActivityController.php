     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request);

        $validated_activity = $request->validate([
            'activity_date' => 'required', 
            'activity' => 'required|max:50',
            'description' => 'required|max:1000',
        ]);
        
        // dd($validated_activity);
        $validated_activity['user_id'] = auth()->user()->id;
        $validated_activity['activity_date'] = Carbon::createFromFormat('d/m/Y',$validated_activity['activity_date'])->format('Y-m-d');
        // dd($validated_activity);

        Activity::create($validated_activity);
        return back()->with('success', 'Activity created successfully.');
        // dd("Activity created");
    }

